function twiceAsOld(dadYearsOld, sonYearsOld) {
  // setup your variable to hold the difference between dad and double the sons age
   //make sure the number is an absolute number
  let numYearsOffDouble = Math.abs(dadYearsOld - (sonYearsOld * 2));
  
  
  //return the difference being stored in the variable
  return numYearsOffDouble;
}